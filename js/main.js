// SWITCH STATEMENT
// DICE VALUES : 1-6

const dice = 2;

switch (dice) {
    case 1:
        console.log("got one");
        break
    case 2:
        console.log("got two");
        break
    case 3:
        console.log("got three");
        break
    default:
        console.log("no roll");
}


